def messageFromBinaryCode(code):
    n = len(code)
    result = ''
    bits = ''
    for i in range(n):
        bits += code[i]
        if (i + 1) % 8 == 0:
            binary = int(bits, 2)
            result += chr(binary)
            bits = ''

    return result


